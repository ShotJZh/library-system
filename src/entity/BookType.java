package entity;

public class BookType {
	private String type_id;
	private String type_name;
	private int type_in;
	private int type_out;
	private int type_sum;
	public String getType_id() {
		return type_id;
	}
	public void setType_id(String type_id) {
		this.type_id = type_id;
	}
	public String getType_name() {
		return type_name;
	}
	public void setType_name(String type_name) {
		this.type_name = type_name;
	}
	public int getType_in() {
		return type_in;
	}
	public void setType_in(int type_in) {
		this.type_in = type_in;
	}
	public int getType_out() {
		return type_out;
	}
	public void setType_out(int type_out) {
		this.type_out = type_out;
	}
	public int getType_sum() {
		return type_sum;
	}
	public void setType_sum(int type_sum) {
		this.type_sum = type_sum;
	}
	
}
